export default function Logo(props: Props): ReactNode {
  const {
    siteConfig: {title},
  } = useDocusaurusContext();
  const {navbar} = useThemeConfig();
  const navbarLogo = navbar.logo as NavbarLogo | undefined;
  const navbarTitle = navbar.title;

  const {imageClassName, titleClassName, ...linkProps} = props;
  const logoLink = useBaseUrl(navbarLogo?.href || '/');

  const fallbackAlt = navbarTitle ? '' : title;
  const alt = navbarLogo?.alt ?? fallbackAlt;
  const height = navbarLogo?.height ?? 32;

  const renderedLogo = (
    <TestBenchLogoSvg
      alt={alt}
      className={navbarLogo?.className}
      height={height}
      style={navbarLogo?.style}
    />
  );

  return (
    <Link
      to={logoLink}
      {...linkProps}
      {...((navbarLogo?.target && {
        target: navbarLogo.target,
      }) || {})}>
      {imageClassName ? (
        <div className={imageClassName}>{renderedLogo}</div>
      ) : (
        renderedLogo
      )}
      {navbarTitle != null && <b className={titleClassName}>{navbarTitle}</b>}
    </Link>
  );
}
